		data() {
			return {
				cross:'',
				ruleForm: {
				yishengzhanghao: '',
				yishengxingming: '',
				keshi: '',
				riqi: '',
				banci: '',
				shangbanshijian: '',
				xiabanshijian: '',
				beizhu: '',
				},
				yishengzhanghaoOptions: [],
				yishengzhanghaoIndex: 0,
				banciOptions: [],
				banciIndex: 0,
				// 登录用户信息
				user: {},
                ro:{
                   yishengzhanghao : false,
                   yishengxingming : false,
                   keshi : false,
                   riqi : false,
                   banci : false,
                   shangbanshijian : false,
                   xiabanshijian : false,
                   beizhu : false,
                },
			}
		},
		components: {
			wPicker,
            xiaEditor,
            multipleSelect
		},
		watch:{
		},
		async onLoad(options) {
            this.ruleForm.riqi = this.$utils.getCurDate();
            this.ruleForm.shangbanshijian = this.$utils.getCurDateTime();

            this.ruleForm.xiabanshijian = this.$utils.getCurDateTime();

			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取


			// 下2
			res = await this.$api.option(`yisheng`,`yishengzhanghao`,{});
			this.yishengzhanghaoOptions = res.data;
			// 自定义下拉框值
			this.banciOptions = "早班,中班,晚班".split(',')

			// 如果有登录，获取登录后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`yishengpaiban`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='yishengzhanghao'){
					this.ruleForm.yishengzhanghao = obj[o];
					this.ro.yishengzhanghao = true;
					continue;
					}
					if(o=='yishengxingming'){
					this.ruleForm.yishengxingming = obj[o];
					this.ro.yishengxingming = true;
					continue;
					}
					if(o=='keshi'){
					this.ruleForm.keshi = obj[o];
					this.ro.keshi = true;
					continue;
					}
					if(o=='riqi'){
					this.ruleForm.riqi = obj[o];
					this.ro.riqi = true;
					continue;
					}
					if(o=='banci'){
					this.ruleForm.banci = obj[o];
					this.ro.banci = true;
					continue;
					}
					if(o=='shangbanshijian'){
					this.ruleForm.shangbanshijian = obj[o];
					this.ro.shangbanshijian = true;
					continue;
					}
					if(o=='xiabanshijian'){
					this.ruleForm.xiabanshijian = obj[o];
					this.ro.xiabanshijian = true;
					continue;
					}
					if(o=='beizhu'){
					this.ruleForm.beizhu = obj[o];
					this.ro.beizhu = true;
					continue;
					}
				}
			}
			this.styleChange()
            this.$forceUpdate()
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					document.querySelectorAll('.radioText').forEach(el => {
					  el.removeAttribute('style')
					  const icon = {"color":"#666666"}
					  Object.keys(icon).forEach((key) => {
						el.style[key] = icon[key]
					  })
					})
					document.querySelectorAll('.radioTextActive').forEach(el => {
					  el.removeAttribute('style')
					  const icon = {"color":"#1D5DCA"}
					  Object.keys(icon).forEach((key) => {
						el.style[key] = icon[key]
					  })
					})
					document.querySelectorAll('.checkText').forEach(el => {
					  el.removeAttribute('style')
					  const icon = {"color":"#666666"}
					  Object.keys(icon).forEach((key) => {
						el.style[key] = icon[key]
					  })
					})
					document.querySelectorAll('.checkTextActive').forEach(el => {
					  el.removeAttribute('style')
					  const icon = {"color":"#1D5DCA"}
					  Object.keys(icon).forEach((key) => {
						el.style[key] = icon[key]
					  })
					})
					document.querySelectorAll('.uni-radio-input').forEach(el => {
					  el.removeAttribute('style')
					  const icon = {"border":"2rpx solid #999999","borderRadius":"100%","background":"#ffffff"}
					  Object.keys(icon).forEach((key) => {
						el.style[key] = icon[key]
					  })
					})
					document.querySelectorAll('.uni-radio-input-checked').forEach(el => {
					  el.removeAttribute('style')
					  const icon = {"border":"2rpx solid #1D5DCA","borderRadius":"100%","background":"#1D5DCA"}
